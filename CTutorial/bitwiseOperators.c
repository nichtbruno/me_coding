#include <stdio.h>

int main() {
    int x = 6; // ->  00000110
    int y = 12; // -> 00001100
    int z = 0; // ->  00000000

    z = x & y; // 00000100 -> 4
    z = x | y; // 00001010 -> 10
    z = x ^ y; // 00001010 -> 10
    z = x << 1; //00001100 -> 12
    z = x >> 1; //00000011 -> 3

    return 0;
}
